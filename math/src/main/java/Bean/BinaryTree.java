package Bean;

public class BinaryTree {
    String Root_data;
    BinaryTree Left_tree;
    BinaryTree Right_tree;



    public String getRoot_data() {
        return Root_data;
    }

    public void setRoot_data(String root_data) {
        Root_data = root_data;
    }

    public BinaryTree getLeft_tree() {
        return Left_tree;
    }

    public void setLeft_tree(BinaryTree left_tree) {
        Left_tree = left_tree;
    }

    public BinaryTree getRight_tree() {
        return Right_tree;
    }

    public void setRight_tree(BinaryTree right_tree) {
        Right_tree = right_tree;
    }
}
